/* 2. Faça um algoritmo para imprimir os números de 1 a 10 utilizando uma estrutura FAÇA/ENQUANTO e um contador. */

#include <stdio.h>

int main(){                                 //Inicio do programa
int numero =1;


do{                                         //Repetir até que o numero chegue em "10"
printf("%d\n", numero);
numero++;
}while(numero<=10);


    return 0;                               //Final do programa
}